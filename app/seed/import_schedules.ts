async function importSchedules() {
  const teacherMap = new Map(teachers.map(t => [t.full_name, t.id]));
  const subjectMap = new Map(subjects.map(s => [s.name, s.id]));
  const sectionMap = new Map(sections.map(s => [s.section_name, s.id]));

  const results = [];
  const errors = [];

  for (const item of data) {
    const teacherId = teacherMap.get(item.teacher_name);
    const subjectId = subjectMap.get(item.subject_name);
    const sectionId = sectionMap.get(item.class_name);

    if (!teacherId || !subjectId || !sectionId) {
      errors.push({ item, reason: 'Missing mapping' });
      continue;
    }

    const { error } = await supabase
      .from('teacher_sections')
      .upsert({
        teacher_id: teacherId,
        subject_id: subjectId,
        section_id: sectionId
      }, { onConflict: 'teacher_id, section_id, subject_id' });

    if (error) {
      errors.push({ item, error: error.message });
    } else {
      results.push(item);
    }
  }

  console.log('Successfully imported:', results.length);
  console.log('Errors:', errors);
}

